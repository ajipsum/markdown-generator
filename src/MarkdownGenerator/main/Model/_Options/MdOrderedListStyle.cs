namespace Grynwald.MarkdownGenerator.Model
{
    public enum MdOrderedListStyle
    {
        /// <summary>
        /// Use dots for ordered list item markers (e.g. "1.", "2." ...) (default)
        /// </summary>
        Dot = 0,

        /// <summary>
        /// Use parentheses for ordered list item markers (e.g. "1)", "2)" ...) 
        /// </summary>
        Parenthesis = 1
    }
}
